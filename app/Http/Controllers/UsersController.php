     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $users = User::all();

        $loggedInUsers = User::whereId(auth()->id())->get();

        foreach ($loggedInUsers as $loggedInUser) {
            $level = $loggedInUser->user_type_id;
        }

        abort_if($level !== 3, 403);

        return view('admin.index', [
            'users' => $users,
            'fullWidth' => true,
        ]);
    }

